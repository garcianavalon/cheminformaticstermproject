package code;
public class ECFFragment {
	private Integer count;
	private Integer key;
	
	public ECFFragment(Integer count, Integer key) {
		super();
		this.count = count;
		this.key = key;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	public Integer getKey() {
		return key;
	}

	public void setKey(Integer key) {
		this.key = key;
	}
	
	
}
